-- ---------- 4.1 nearby_tenants ----------

CREATE OR REPLACE FUNCTION public.nearby_tenants(
  user_lat   double precision,
  user_lng   double precision,
  radius_km  int DEFAULT 5
)
RETURNS TABLE (
  id            uuid,
  nama          text,
  foto_url      text,
  nomor_wa      text,
  latitude      double precision,
  longitude     double precision,
  alamat        text,
  jam_buka      text,
  rating        numeric,
  dist_meters   double precision
)
LANGUAGE sql
STABLE
SECURITY INVOKER
SET search_path = public, extensions
AS $$
  SELECT
    t.id, t.nama, t.foto_url, t.nomor_wa,
    t.latitude, t.longitude, t.alamat, t.jam_buka, t.rating,
    extensions.ST_Distance(
      t.location,
      extensions.ST_SetSRID(extensions.ST_MakePoint(user_lng, user_lat), 4326)::extensions.geography
    ) AS dist_meters
  FROM public.tenants t
  WHERE t.is_active = true
    AND extensions.ST_DWithin(
      t.location,
      extensions.ST_SetSRID(extensions.ST_MakePoint(user_lng, user_lat), 4326)::extensions.geography,
      radius_km * 1000
    )
  ORDER BY t.location <-> extensions.ST_SetSRID(extensions.ST_MakePoint(user_lng, user_lat), 4326)::extensions.geography;
$$;


-- ---------- 4.2 tenants_in_view ----------

CREATE OR REPLACE FUNCTION public.tenants_in_view(
  min_lat   double precision,
  min_lng   double precision,
  max_lat   double precision,
  max_lng   double precision
)
RETURNS TABLE (
  id          uuid,
  nama        text,
  foto_url    text,
  nomor_wa    text,
  latitude    double precision,
  longitude   double precision
)
LANGUAGE sql
STABLE
SECURITY INVOKER
SET search_path = public, extensions
AS $$
  SELECT t.id, t.nama, t.foto_url, t.nomor_wa, t.latitude, t.longitude
  FROM public.tenants t
  WHERE t.is_active = true
    AND t.location operator(extensions.&&)
        extensions.ST_SetSRID(
          extensions.ST_MakeBox2D(
            extensions.ST_Point(min_lng, min_lat),
            extensions.ST_Point(max_lng, max_lat)
          ),
          4326
        );
$$;


-- ---------- 4.3 is_super_admin ----------

CREATE OR REPLACE FUNCTION public.is_super_admin()
RETURNS boolean
LANGUAGE sql STABLE
AS $$
  SELECT COALESCE(
    (auth.jwt() -> 'app_metadata' ->> 'role') = 'super_admin',
    false
  );
$$;


