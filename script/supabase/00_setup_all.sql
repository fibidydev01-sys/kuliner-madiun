-- ---------- 3.2 slug auto-generate ----------
-- Slugify nama → kebab-case + suffix angka kalau bentrok.
-- Dipakai untuk routing /[slug]/page.tsx (SEO-friendly URL).
--
-- ⚠️ ORDERING: unaccent_safe() HARUS dideklarasi DULUAN sebelum slugify(),
-- karena LANGUAGE sql validate dependency saat CREATE (beda dengan plpgsql
-- yang lazy). Kalau di-swap, akan error "function unaccent_safe does not exist".

-- Fallback unaccent kalau extension unaccent ga aktif.
-- (Kita ga enable unaccent supaya ga nambah dependency — pakai stripper sederhana).
CREATE OR REPLACE FUNCTION public.unaccent_safe(input text)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT translate(
           coalesce(input, ''),
           'àáâãäåèéêëìíîïòóôõöùúûüñçÀÁÂÃÄÅÈÉÊËÌÍÎÏÒÓÔÕÖÙÚÛÜÑÇ',
           'aaaaaaeeeeiiiiooooouuuuncAAAAAAEEEEIIIIOOOOOUUUUNC'
         );
$$;

CREATE OR REPLACE FUNCTION public.slugify(input text)
RETURNS text
LANGUAGE sql
IMMUTABLE
AS $$
  SELECT regexp_replace(
           regexp_replace(
             lower(public.unaccent_safe(coalesce(input, ''))),
             '[^a-z0-9]+', '-', 'g'
           ),
           '(^-+|-+$)', '', 'g'
         );
$$;

CREATE OR REPLACE FUNCTION public.tenants_set_slug()
RETURNS trigger
LANGUAGE plpgsql
AS $$
DECLARE
  base_slug   text;
  candidate   text;
  counter     int := 1;
BEGIN
  -- Hanya generate slug kalau:
  --   - INSERT dan slug NULL/empty
  --   - UPDATE dan nama berubah dan slug NULL/empty
  IF (TG_OP = 'INSERT' AND (NEW.slug IS NULL OR NEW.slug = ''))
     OR (TG_OP = 'UPDATE' AND NEW.nama IS DISTINCT FROM OLD.nama
         AND (NEW.slug IS NULL OR NEW.slug = '')) THEN

    base_slug := public.slugify(NEW.nama);
    IF base_slug = '' THEN
      base_slug := 'warung';
    END IF;

    candidate := base_slug;

    -- Loop sampai dapat slug unik
    WHILE EXISTS (
      SELECT 1 FROM public.tenants
      WHERE slug = candidate
        AND (TG_OP = 'INSERT' OR id <> NEW.id)
    ) LOOP
      counter := counter + 1;
      candidate := base_slug || '-' || counter;
    END LOOP;

    NEW.slug := candidate;
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS tenants_set_slug_trigger ON public.tenants;
CREATE TRIGGER tenants_set_slug_trigger
  BEFORE INSERT OR UPDATE OF nama ON public.tenants
  FOR EACH ROW EXECUTE FUNCTION public.tenants_set_slug();


