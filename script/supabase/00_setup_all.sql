-- ---------- 2.2 tenants ----------

CREATE TABLE IF NOT EXISTS public.tenants (
  id              uuid PRIMARY KEY DEFAULT extensions.uuid_generate_v4(),
  nama            text NOT NULL,
  slug            text UNIQUE,                            -- auto-generated by trigger
  deskripsi       text,
  foto_url        text,
  foto_public_id  text,
  nomor_wa        text NOT NULL,
  latitude        double precision NOT NULL,
  longitude       double precision NOT NULL,
  location        extensions.geography(POINT, 4326)
                  GENERATED ALWAYS AS (
                    extensions.ST_SetSRID(
                      extensions.ST_MakePoint(longitude, latitude), 4326
                    )::extensions.geography
                  ) STORED,
  alamat          text,
  jam_buka        text,
  hari_libur      text[] DEFAULT ARRAY[]::text[],
  rating          numeric(2,1) CHECK (rating BETWEEN 0 AND 5),
  total_review    int CHECK (total_review >= 0),
  is_active       boolean NOT NULL DEFAULT true,
  created_at      timestamptz NOT NULL DEFAULT now(),
  updated_at      timestamptz NOT NULL DEFAULT now(),

  CONSTRAINT tenants_lat_check  CHECK (latitude  BETWEEN -90  AND 90),
  CONSTRAINT tenants_lng_check  CHECK (longitude BETWEEN -180 AND 180),
  CONSTRAINT tenants_wa_check   CHECK (nomor_wa ~ '^62[0-9]{8,14}$')
);

CREATE INDEX IF NOT EXISTS idx_tenants_location  ON public.tenants USING GIST (location);
CREATE INDEX IF NOT EXISTS idx_tenants_active    ON public.tenants (is_active) WHERE is_active = true;
CREATE INDEX IF NOT EXISTS idx_tenants_nama_trgm ON public.tenants USING GIN (nama extensions.gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_tenants_slug      ON public.tenants (slug);


